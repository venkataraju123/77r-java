package blc_elc;

public class Librarybook {
	int bookId;
	String title;
	String author;
	double price;
	int getbookId() {
		return bookId;
	}
	String gettitle() {
		return title;
	}
String getauthor() {
	return author;
}
double getprice() {
	return price;
}
void setbookId(int bId) {
	bookId=bId;
}
void settitle(String titl) {
	title=titl;
}
void setauthor(String aut) {
	author=aut;
}
void setprice(double pri) {
	price=pri;
}
}
